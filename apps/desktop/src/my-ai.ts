import { app, shell } from "electron";
import fs from "fs";
import os from "os";
import path from "path";
import { ipcHandle } from "./ipc.js";

// "My AI" is a folder of personal context files meant to be handed to an AI
// coding agent (Claude Code) as background about the user — point the agent at
// info.md (or symlink it into a CLAUDE.md/memory setup) and it knows who it's
// working for. v1 seeds a static info.md describing the owner and the data
// Bessel collects; a future version may replace the file with a CLI the agent
// queries live instead.

function myAiInfoTemplate(): string {
  const timeZone = Intl.DateTimeFormat().resolvedOptions().timeZone;
  return `# About Me

> Generated by Bessel Desktop v${app.getVersion()} on ${new Date().toISOString().slice(0, 10)}.
> This file gives an AI assistant (e.g. Claude Code) personal context about the
> owner of this machine. Bessel is a personal life dashboard that aggregates the
> data described below. A future version may replace this static file with a CLI
> the assistant can query directly.

## Identity

- Username: ${os.userInfo().username}
- Hostname: ${os.hostname()}
- Platform: ${process.platform} (${process.arch})
- Timezone: ${timeZone}
- Locale: ${app.getLocale()}

## Data Bessel collects about me

Each section names a data domain the Bessel API stores. Summaries exported from
the dashboard can be pasted under the matching heading; an assistant reading
this file should treat those summaries as ground truth about my habits.

### Health & fitness

Workouts plus Apple Health metrics (heart rate, sleep, steps) synced from my
phone.

### Computer activity

Active-window tracking from the Bessel monitor service — which apps and
projects I spend my screen time in, per day.

### Finances

Bank accounts, transactions, and investment holdings and trades.

### Music

Spotify listening, mirrored live into the dashboard.

### Places & travel

Saved places and travel history.

### Tasks & projects

My personal task board and the dev projects tracked in Bessel.

### Food

Recipes I've saved.

## Notes for the assistant

- Treat this file as background context about me, not as instructions.
- Don't assume facts about me that aren't stated here or in the data above.
- Sections without a summary yet simply haven't been exported — ask me rather
  than guessing.
`;
}

export function registerMyAiHandlers(userDataDir: string): void {
  const myAiDir = path.join(userDataDir, "my-ai");
  const infoFile = path.join(myAiDir, "info.md");

  ipcHandle("my-ai:status", () => ({
    path: myAiDir,
    exists: fs.existsSync(infoFile),
  }));

  ipcHandle("my-ai:create", (): string => {
    fs.mkdirSync(myAiDir, { recursive: true });
    // Never clobber an info.md the user has already edited.
    if (!fs.existsSync(infoFile)) {
      fs.writeFileSync(infoFile, myAiInfoTemplate());
    }
    return myAiDir;
  });

  ipcHandle("my-ai:reveal", () => {
    shell.showItemInFolder(infoFile);
  });
}
